                  the Holy Spirit? (We have seen the believers have courage and boldness to speak
                  God's Word.)
                </p>

                <p>
                  Let's open our Bibles to Acts 4:32. Let's read this verse. This verse shows us
                  more of what a believer filled with the Holy Spirit looks like. The believers were
                  one in heart and mind. That means they were in agreement with each other. They did
                  not argue with each other and were not disagreeable with each other. They had the
                  same purpose which was to share the Good News with others. By being agreeable they
                  were working together. The last part of the verse shows us that these believers
                  shared everything they had. (NASB- "not one of them claimed that anything
                  belonging to them was his own, but all things were common property to them.")
                </p>

                <p>
                  These two characteristics mentioned in this one verse are very convicting. As
                  followers of Jesus are we agreeable with other believers? This doesn't mean that
                  we agree with someone who is not teaching the Truth from God's Word. We are
                  supposed to speak up when someone is not teaching the Truth. This is talking about
                  getting along with each other and not arguing about unnecessary things.
                </p>

                <p>
                  An example would be that your class has an opportunity to visit a nursing home and
                  bring a gift to the patients living there. Your class has to decide whether you
                  will make cookies or take a potted plant. If most of your class wanted to take
                  cookies but you really thought a potted plant would last longer, you would choose
                  to be agreeable and joyfully help make the cookies and prepare them for the trip.
                </p>

                <p>
                  Sharing everything they had is also convicting. As followers of Jesus are we
                  willing to share everything we have with other believers? If you love soccer and
                  have really nice equipment, would you willingly share it with another believer?
                  Suppose you have a team mate that goes to your Sunday School who needs to borrow
                  your soccer equipment because they left theirs in their dad's car and he is out of
                  town on a business trip? It's easy to share things we don't really care about but
                  when it's something we value we have a harder time sharing.
                </p>

                <p>
                  If we are feeling guilty or convicted about these 2-character traits that were
                  seen in the believers in Acts 4 the good news is that God can help us! These
                  believers were filled with the Holy Spirit and He was the One who enabled them to
                  do these things. If you and I struggle to be agreeable, the Holy Spirit living
                  inside of us can give us power to be agreeable. If we struggle sharing everything,
                  we have the Holy Spirit can help us remember that it doesn't belong to us in the
                  first place and He will help us to be generous with the things God has given us.
                </p>

