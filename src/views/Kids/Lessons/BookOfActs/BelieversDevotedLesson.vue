                  As we look at Act 4:33-37 we see that the believers took care of one another and
                  helped anyone who had a need. If they needed to sell houses or lands to help
                  another believer, they did that. They brought the money to the apostles who gave
                  the money to the believers who had a need. Let's see the name of one of the
                  believers who sold his field to share his money with others.
                </p>

                <p>
                  Read Acts 4:36-37. These believers were devoted to following Jesus. They had a
                  sincere desire to live a life that pleased God. As we see a sincere desire for the
                  believers to help each other and the apostles continuing to share the Good News
                  about Jesus we are about to see some people who pretend to be devoted followers of
                  Jesus.
                </p>

                <p>
                  Let's read Acts 5:1-2. In your own words what do you see that Ananias and Sapphira
                  did in these verses? (Sold a piece of property and kept some of the money back for
                  themselves and took the rest to the apostles.) A devoted follower of Christ is
                  trustworthy. Ananias and Sapphira were dishonest when they brought their money to
                  the apostles. They made it appear as they were giving all the money they received
                  from selling their property. The Holy Spirit enabled Peter to know that they were
                  being dishonest.
                </p>

                <p>
                  The work of spreading the Good News about Jesus is important. What Ananias and
                  Sapphira did was sin. They wanted to appear as though they were generous by
                  sharing the money from their property. God knew their heart and punished both of
                  them for their sin.
                </p>

                <p>
                  Read Acts 5:4-5 and 5:9-10. The consequences of their sin was an example to others
                  that God knows when a person is pretending to be devoted or is a sincere follower
                  of Christ. A picture of what the power of the Holy Spirit looks like in the
                  believer's life is to think of a garden hose. The Holy Spirit is the water that
                  flows through the hose (the believer's life). When a believer allows sin in
                  his/her life it is like a kink in the hose. The water can still get through but
                  the strength of the water is not as strong.
                </p>

                <p>
                  Satan, God's enemy tries to allow sin to keep God from doing powerful things in
                  the life of believers. It's important to confess any sin that is in our life and
                  turn from it so the Holy Spirit's power can enable us to serve with His full
                  power. If Ananias' and Sapphira's sin was not disciplined the power of the Holy
                  Spirit would be affected. When their sin was punished the power of the Holy Spirit
                  continued to work in a mighty way through the apostles. Many people heard the Good
                  News and believed in the Lord Jesus and were saved. The number of believers
                  continued to grow.
                </p>

                <p>
                  Many sick people were brought to the apostles to be healed. Read Acts 5:16. Every
                  sick person was healed. When Jesus was on the earth, He healed people who were
                  brought to Him. Now that He had returned to heaven the Holy Spirit living inside
                  of the apostles was healing the sick that were brought to them.
                </p>

                <p>
                  While God is working and people are being saved, once again the religious leaders
                  do not like it. It would seem that if you were a religious leader, you would be
                  happy to see people coming to God to be saved and sick people being healed. Not so
                  with these leaders.
                </p>

                <p>
                  Let's read Acts 5:17 and see how they felt about what was happening. Why do you
                  think they were jealous? These men claimed to know God and were the leaders of the
                  people to teach them about God. Did they have the same kind of power that the
                  apostles had? (No) They couldn't have that power because they didn't believe that
                  Jesus was the Son of God. Because they didn't believe in Jesus, they could not
                  have the Holy Spirit living inside of them giving them the power to do things for
                  God.
                </p>

                <p>
                  The religious leaders had the apostles arrested and thrown into jail. God's enemy
                  tries to stop God's work by making life difficult for the followers of Jesus. That
                  should not discourage followers of Jesus. Believers can have courage to follow
                  Jesus because nothing can stop God's Work. The Holy Spirit living in the hearts of
                  believers gives them courage to trust God even when they are arrested and thrown
                  in jail for sharing the Good News.
                </p>

                <p>
                  Let's read Acts 5:19. The apostles were taken out of the jail leaving the prison
                  guards standing at the locked doors. The angel told the apostles to go to the
                  temple and continue to share the Good News. The apostles obeyed and went to the
                  temple to preach.
                </p>

                <p>
                  Imagine the religious leaders' shock when they wanted the apostles to be brought
                  from jail to stand before them. Acts 5:22-23 tells us what message they received.
                  This news puzzled the religious leaders.
                </p>

                <p>
                  Someone told the leaders that the apostles were teaching in the temple courts. A
                  captain and his officers went and brought the apostles before the religious
                  leaders. The religious leaders were frustrated with the apostles because they had
                  given them strict orders not to teach in the Name of Jesus. The leaders were
                  getting tired of hearing that it was because of them that Jesus was put to death.
                  "You are determined to make us guilty of this Man's blood." They wanted this
                  message to stop.
                </p>

                <p>
                  The apostles were not afraid of these men because they were filled with the Holy
                  Spirit. They spoke boldly that they must obey God not men and shared the Good News
                  about Jesus with them. This made the religious leaders so angry that they wanted
                  to put the apostles to death.
                </p>

                <p>
                  A man named Gamaliel gave the religious leaders some advice. He told them that if
                  the work that the apostles was just the work of men it wouldn't amount to
                  anything, but if it was truly the work of God no one could stop it and if they
                  tried, they would be fighting against God. He suggested that the leaders leave the
                  apostles alone. They followed Gamaliel's advice to leave them alone but they just
                  couldn't let the apostles go. Let's read what they did to the apostles in verse
                  Acts 5:40. (To be flogged is to be whipped 39 times.)
                </p>

                <p>
                  Did the apostles do anything wrong to deserve to be beaten? Does this remind you
                  of Someone who was beaten and was completely innocent? Jesus suffered cruelty at
                  the hands of the religious leaders and He is the innocent Son of God. The apostles
                  left there feeling privileged to suffer because they were followers of Jesus. They
                  did not run off and hide because they were afraid to suffer. They faithfully
                  continued to share the Good News about Jesus day after day.
                </p>

                <p>
                  Our study in the Book of Acts is teaching us how to live a life that pleases God.
