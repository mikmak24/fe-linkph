                </div>
                <div class="section-content">
                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">I Jesus Christ as God is Equal to the Father — 1 John 2:23</h4>
                    <p class="mb-2"><strong>John 5:18</strong> "Therefore the Jews sought the more to kill Him, because He not only had broken the Sabbath, but said also that God was His Father, making Himself equal with God." The Jews who were strong believers in Deuteronomy 6:4, understood Christ's meaning.</p>
                    <p class="mb-2">The Jews recognized but one God, "The Lord our God is one Lord," and considered Christ's claims as blasphemous and worthy of death by stoning, Leviticus 24:12.</p>
                    <p class="mb-2"><strong>John 10:30</strong> "I and My Father are one," is certainly a proof of His equality with God.</p>
                    <p class="mb-2"><strong>John 14:9</strong> "He that hath seen Me hath seen the Father; and how sayest thou (Philip) then, Shew us the Father?" They are one and indivisible.</p>
                    <p class="mb-2"><strong>Colossians 1:15</strong> "Who (Christ) is the image of the invisible God ..." Jesus Christ is the visible reflection of the invisible God — invisible because He is a Spirit.</p>
                    <p class="mb-2"><strong>John 10:33</strong> "For a good work we stone Thee not; but for blasphemy; and because that Thou being a man, makest Thyself God."</p>
                    <p class="mb-2">Jesus was trying to prove to them that He was God but they stubbornly refused to believe.</p>
                    <p class="mb-2"><strong>Philippians 2:5,6</strong> "... Christ Jesus: who, being in the form of God, thought it not robbery to be equal with God." Jesus was honestly equal with the Father God.</p>
                    <p class="mb-2"><strong>John 17:5</strong> "And now, O Father, glorify Thou Me with Thine own self with the glory which I had with Thee before the world was." Jesus refers to His pre-incarnate glory.</p>
                    <p class="mb-2"><strong>John 1:1</strong> "In the beginning was the Word (Christ), and the Word was with God, and the Word was God." This is a strong positive statement to open John's Gospel.</p>
                    <p class="mb-2"><strong>1 John 5:20</strong> "... that we may know Him that is true, and we are in Him that is true, even in His Son Jesus Christ. This is the true God and eternal life."</p>
                    <p class="mb-2">John both in his Gospel and epistles states the fact repeatedly that Jesus is God.</p>
                    <p class="mb-2">Titus joins John in his testimony. <strong>Titus 2:13</strong> "Looking for the blessed hope, and the glorious appearing of the great God and our Savior Jesus Christ." (One person only)</p>
                    <p class="mb-2">The One that we wait for is God the Savior who came the first time to Bethlehem.</p>
                    <p class="mb-2">The writer of Hebrew also adds his testimony. <strong>Hebrews 1:8</strong>, "But unto the Son He saith, Thy throne, O God, is for ever and ever." God the Father calls His Son "God."</p>
                    <p class="mb-2">Jude in his great benediction calls the Savior God. <strong>Jude 25</strong>, "To the only wise God our Savior." This refers to but one Person, the Lord Jesus Christ.</p>
                    <p class="mb-2">One of the difficult verses to understand is <strong>Colossians 2:9</strong>, "For in Him (Christ) dwelleth all the fullness of the Godhead bodily." This verse is too immense for mortal man.</p>
                    <p class="mb-2">To say that He is only "a" son of God is to insult and blaspheme the Savior.</p>
                    <p class="mb-2">At the Transfiguration in Matthew 17:2, we get a glimpse of the glory of the pre-incarnate Christ. "His face did shine as the sun."</p>
                    <p class="mb-2">Whoever refuses to accept Jesus as God, equal to the Father, is guilty of the enormous sin of rejecting the Word of God.</p>
                    <p class="mb-2">To question or deny the eternal pre-existence and glorious pre-incarnation of Christ does not lessen the fact that Jesus is and had always been God.</p>
                    <p class="mb-2">The one who questions these facts is guilty of awful slander against the Savior.</p>
                    <p class="mb-2">Let us rather glory in the fact that though He were true God, equal with the Father, that He emptied Himself of His majestic garments of Divinity for our sakes.</p>
                    <p class="mb-0">Let us fall before Him in worship, praise and adoration for His great sacrifice.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">II Jesus Christ as Man is Subordinated to the Father</h4>
                    <p class="mb-2">When Jesus Christ became man He voluntarily took the lower place — a place of humility and subordination to the Father God.</p>
                    <p class="mb-2"><strong>John 14:28</strong> "Ye have heard how I said unto you, I go away, and come again unto you. If ye loved Me, ye would rejoice, because I said, I go unto the Father: for My Father is greater than I." Jesus the human, is speaking of the eternal God.</p>
                    <p class="mb-2">When Jesus was conceived of the Holy Spirit, then Christ entered into a new relationship with God. <strong>Hebrews 1:5</strong> "For unto which of the angels said He (the Father) at any time, Thou art My Son, this day have I begotten Thee?" This occurred in Luke 1:35.</p>
                    <p class="mb-2">John 3:16 refers to Jesus as "only begotten Son." This was prophesied in Psalm 2:7.</p>
                    <p class="mb-2">Does this begetting of the Son refer to the origin of the eternal God, the Lord Jesus?</p>
                    <p class="mb-2">Certainly not, for as God He was never born. He always existed, Isaiah 9:6,7.</p>
                    <p class="mb-3">The begetting refers to that which transpired in Luke 1:35, when the Holy Spirit came upon the Virgin Mary and the human Jesus was conceived, Luke 1:35.</p>

                    <h5 class="text-primary mb-3">A. Ways in which the Son was subordinated to the Father</h5>
                    <ol class="mb-3 ms-3">
                      <li class="mb-2">Jesus' earthly life was lived because of the Father: John 6:57 "As the living Father hath sent Me, I live by the Father." The Father sustained the Son.</li>
                      <li class="mb-2">Jesus could not do anything independently of the Father: John 5:19 "The Son can do nothing of Himself; but what He seeth the Father do: for what things soever He doeth, these also doeth the Son likewise."</li>
                      <li class="mb-2">Jesus Christ was sent to earth by the Father. John 6:29 "This is the work of God, that ye believe on Him whom He hath sent." Also John 8:29, 42.</li>
                      <li class="mb-2">The Father gave the Son authority and directions: John 10:18 "I lay it (My life) down of Myself. I have power to lay it down, and I have power to take it again. This commandment have I received of My Father." Also John 13:3.</li>
                      <li class="mb-2">Jesus Christ received His messages from the Father: John 8:26 "I speak to the world those things which I have heard of Him."</li>
                      <li class="mb-2">The Father gave to the Savior certain works to accomplish: John 5:36 "The works which the Father hath given Me to finish, the same works that I do, bear witness of Me, that the Father hath sent me." Also John 17:4.</li>
                      <li class="mb-2">The Father assigned a kingdom to the Son: Luke 22:29 "And I appoint unto you a kingdom, as My Father hath appointed unto Me."</li>
                      <li class="mb-2">During this present Church age Jesus is subjected to the headship of the Father: 1 Corinthians 11:3 "The head of the woman is the man; and the head of Christ is God."</li>
                      <li class="mb-2">Christ has become the way by which men are able to approach God: Hebrews 7:25 "Wherefore He is able also to save them to the uttermost that come unto God by Him, seeing He ever liveth to make intercession for them."</li>
                      <li class="mb-2">Jesus has become the way to God, the way of salvation. He is the only way: John 14:6.</li>
                      <li class="mb-0">Jesus referred to the Father as "My God." John 20:17 "Jesus saith unto her ... I ascend unto ... My God." Also in Matthew 27:46, the fourth saying from the cross.</li>
                    </ol>

                    <h5 class="text-primary mb-3">B. The extent of this subordination — how long will it last?</h5>
                    <p class="mb-2">Luke 22:29 says that the Father gave the Son a kingdom. The Son will return this to the Father.</p>
                    <p class="mb-2">1 Corinthians 15:24 "Then cometh the end, when He shall have delivered up the kingdom to God, even the Father."</p>
                    <p class="mb-2">The period of subordination extends beyond this — beyond the consummation of this age, even beyond the Great White Throne judgment.</p>
                    <p class="mb-0">1 Corinthians 15:27,28 "For He hath put all things under His feet ... then shall the Son also Himself be subject unto Him that put all things under Him, that God may be all in all."</p>
                  </div>
                </div>
              </div>

