                <div class="section-header">
                  <i class="bi bi-bookmark-check section-icon"></i>
                  <h3 class="section-title">Conclusion</h3>
                </div>
                <div class="section-content">
                  <div class="content-card">
                    <p class="mb-2">If we understand the Scriptures aright there is no conflict here. The pre-incarnate Christ is and always has been equal to the Father God Almighty.</p>
                    <p class="mb-2">The human incarnate Christ laid aside this position and in His self-humiliation chose a place of subordination to the Father as a child to his parent.</p>
                    <p class="mb-2">The more we study the sevenfold humbling of Christ (Philippians 2:5-8), the more we see manifested His tremendous love for us while we were still ugly sinners.</p>
                    <p class="mb-0">Let us hasten this wonderful message to all nations.</p>
