                    <p>This is a controversial point with many cults who deny the deity of Christ.</p>
                    <p>They say that Jesus, like us, is a son of God, but not God. They maintain that He is a son who is subservient to the Father and quote Scriptures to prove it.</p>
                    <p>How do we evangelicals answer their seemingly unanswerable arguments?</p>
                    <p>I believe that in brief the answer is this: As God, Jesus in His pre-incarnate state was, is, and always will be equal with God the Father.</p>
                    <p>However, in His incarnate state He is definitely subservient to the Father.</p>
                    <p class="mb-0">A study of the pre-incarnate Christ will help us to appreciate what the Son emptied Himself of in order to become our Savior, Philippians 2:5-8.</p>
                  </div>
