                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">THE ANGEL OF THE LORD</h4>
                    <p class="mb-3">A number of references in the Old Testament single out a being identified as "the angel of the Lord." This distinctive personage is especially active at critical times in Old Testament history and is identified when contact with angels involves revelation of some special message from God to man. The angel of the Lord is linked with key events in the lives of each of the patriarchs (Abraham and Isaac, <strong>Ge 22:11, 15-18</strong>; Jacob, <strong>Ge 31:11</strong>).</p>
                    <ul class="scripture-list">
                      <li>The angel of the Lord called Moses to his mission (<strong>Ex 3</strong>), appeared to Gideon (<strong>Jdg 6</strong>), empowered Samson (<strong>Jdg 13</strong>), strengthened Elijah (<strong>1 Ki 19</strong>), and was the agent of revelation in a prophecy about Israel's future (<strong>Zec 1</strong>).</li>
                      <li>He can be called on to protect (<strong>Ps 34:7</strong>) and to do battle against implacable enemies (<strong>Ps 35:5-6</strong>). There has been much speculation about the identity of the angel of the Lord.</li>
                      <li>Many suggest that he is actually the Second Person of the Trinity, appearing before the Incarnation. This interpretation is supported by the fact that the angel of the Lord not only serves as the agent of revelation in most contexts where he appears; he also speaks as the God of the covenant (<strong>Jdg 2:1-4</strong>). He is often viewed as God by those to whom he appears (<strong>Ge 16:9-13; Ex 3:2, 6; Jdg 13:20-22</strong>).</li>
                      <li>Two conclusions seem evident: no clear distinction can be made between this angel and Yahweh, and where human beings encounter God in the Old Testament, they meet Him not in unmasked glory but in the person of the angel of the Lord.</li>
                    </ul>
                  </div>

                  <!-- ANGELS IN THE NEW TESTAMENT -->
                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">ANGELS IN THE NEW TESTAMENT</h4>
                    <p class="mb-3">The New Testament assumes all that the Old Testament teaches about angels and goes on to add fresh information.</p>
                    <p class="mb-3">The New Testament word <em>angelos</em> also means "messenger." It is used some 175 times. The activity of angels in the New Testament concentrated around the birth of Christ (<strong>Mt 1-2; Lk 1-2</strong>) and will again be prominent at Jesus' return and the judgment to take place then (cf. <strong>Mt 13, 24-25, 1 Th 1, Rev</strong>). Jesus' statement that those raised from the dead are "like the angels" (<strong>Mt 22:30; Mk 12:25; Lk 20:36</strong>) simply affirms that, like angels, the resurrected are not subject to the limitations of mortal life on earth.</p>
                    <ul class="scripture-list">
                      <li>The New Testament makes explicit what is hinted at in the Old Testament. Satan leads a host of angels (<strong>Mt 25:41</strong>). Like him, they fell from their original state; they choose not to "keep their positions of authority but abandoned their own home" (<strong>Jude 6</strong>) and as a result are condemned to eternal judgment (also <strong>2 Pe 2:4</strong>).</li>
                      <li>Many believe that fallen angels are the demons that are spoken of in the New Testament. The angels who remained committed in their allegiance to God continue to serve Him and carry out the missions they are assigned. As noted above, the New Testament associates angels closely with the first and second comings of Jesus. In the meanwhile, they are "all ministering spirits sent to serve those who will inherit salvation" (<strong>Heb 1:14</strong>).</li>
                      <li>Angels seem to have a special ministry in relation to children (<strong>Mt 18:10</strong>). An angel was instrumental in releasing Peter from prison (<strong>Acts 12</strong>) and in preparing the way for the conversion of Cornelius (<strong>Ac 10-11</strong>).</li>
                      <li>Angels in the unseen world are all under Jesus, "who is the head over every power and authority" (<strong>Col 2:10</strong>). Jesus, not angels, is the focus of our faith. What we glimpse about angels is intriguing and stimulates speculation. But the thrust of the Old Testament and New Testament is clear.</li>
                    </ul>
