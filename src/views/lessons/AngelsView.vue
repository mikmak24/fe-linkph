                  <!-- ANGELS IN THE OLD TESTAMENT -->
                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">ANGELS IN THE OLD TESTAMENT</h4>
                    <ul class="scripture-list">
                      <li>The Hebrew word <em>mal'ak</em> means "messenger," "representative," or "angel." It is used of both human and supernatural messengers, and the context will usually determine which is intended.</li>
                      <li>The mission of a <em>mal'ak</em> is to (1) carry a message, (2) fulfill a special, specific commission, and/or (3) represent the one sending him.</li>
                      <li>The supernatural beings called angels are also referred to by other names. They are called "sons of God" (KJV), a phrase meaning direct creations of God (<strong>Job 1:6; 2:1</strong>; and possibly <strong>Ge 6:2-4</strong>).</li>
                      <li>They are also called "mighty ones" in <strong>Ps 29:1</strong> and "heavenly beings" in <strong>Ps 89:6</strong>, as well as "holy ones" in <strong>Ps 89:5, 7; Da 4:13, 17, 23; 8:13</strong> (twice).</li>
                      <li>The angels were created by God and were witnesses to the creation of the material universe (<strong>Job 38:7</strong>). They serve as members of God's eternal court (<strong>Job 1:6; Isa 6:2-4</strong>), and they are exhorted to praise Him (<strong>Ps 103:20-21; 148:2</strong>).</li>
                      <li>There are different orders and different types of these powerful beings. The cherubim, for example, have traits of both humans and animals (<strong>Ge 3:24; Ps 18:10; Isa 6:2; Eze 1:5-14; 10:19-22</strong>). One powerful archangel, Gabriel, is identified by name four times in Scripture (<strong>Da 8:16; 9:21; Lk 1:19, 26</strong>). Satan was created an angel and named Lucifer.</li>
                      <li>The New Testament tells us what the Old Testament hints at. Satan led a great rebellion in the unseen universe and was followed by many of the angels who fell with him.</li>
                    </ul>
                    <p class="mt-3">While angels are not a major theme of the Old Testament, there are many indications of their importance to sacred history. Angels were associated with God's deliverance of His people from slavery in Egypt. God promised Israel, "I am sending an angel ahead of you to guard you along the way and to bring you to the place I have prepared" (<strong>Ex 23:20</strong>). On a similar mission of protection an angel aided Shadrach, Meshach, and Abednego in the fiery furnace (<strong>Da 3:28</strong>) and Daniel in the lions' den (<strong>6:22</strong>).</p>
                    <p>One of the most graphic Old Testament stories of angelic protection is found in <strong>2 Ki 6</strong>. The king of Aram had sent an army to capture the prophet Elisha. Elisha's servant stepped outside one morning to find the town of Dothan surrounded by an enemy force. He ran in terror to the prophet. Elisha quieted his fears and then asked God to open the servant's eyes. Suddenly the servant saw "the hills full of horses and chariots of fire all around Elisha" (<strong>6:17</strong>). An angelic army was present to protect the Lord's prophet.</p>
                    <p>In addition, angels were involved in God's dramatic judgments on sinful people (Sodom, <strong>Ge 19:1</strong>; a plague decimating Israel, <strong>2 Sa 24:17; 1 Ch 21:15</strong>; destruction of an Assyrian army, <strong>2 Ch 32:21; Isa 37:36</strong>). Abraham reassured his servant by saying that God would send an angel to help the servant get a wife for Isaac (<strong>Ge 24:7, 40</strong>). Angels clearly are given assignments to guard and guide believers and to carry out God's judgments on sin.</p>
                  </div>

