                    <p class="mb-2"><strong>2 Samuel 23:3</strong> "He that ruleth over men must be just, ruling in the fear of God."</p>
                    <p class="mb-2">Lack of absence of this fear can only bring sin and disillusionment, and end in tragedy.</p>
                    <p class="mb-2"><strong>Genesis 20:11</strong>, Abraham sinned because, "I thought, Surely the fear of God is not in this place." He mistakenly thought that he was beyond God's fear and was really free.</p>
                    <p class="mb-0"><strong>Romans 3:18</strong> "There is no fear of God before their eyes" — a picture of degenerate man.</p>
