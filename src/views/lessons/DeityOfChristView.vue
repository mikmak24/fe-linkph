                  <div class="intro-card">
                    <p>This fundamental doctrine is consistently attacked by Manalistas, Russelites, etc.</p>
                    <p>Belief in the deity of Christ is essential to salvation. Romans 10:9 RV, "That if thou shalt confess with thy mouth Jesus as Lord ..." not merely a good man.</p>
                    <p>A study of Bible conversions will reveal that many occurred when they recognized Jesus as Lord — Paul in Acts 9:5, the man in John 9:38, etc.</p>
                    <p>To deny the deity of Christ is to rob mankind of a Savior and condemn us eternally.</p>
                    <p class="mb-0">In this lesson we present fifteen proofs of the deity of Christ.</p>
                  </div>
