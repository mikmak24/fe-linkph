                  <h3 class="section-title">Main Content</h3>
                </div>
                <div class="section-content">
                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">I Jesus Christ is Referred to as Lord</h4>
                    <p class="mb-2">Psalm 110:1 "The Lord said unto my Lord, Sit Thou at My right hand until I make Thine enemies Thy footstool." (The Lord, The Father God, said to my Lord, the Savior ...)</p>
                    <p class="mb-2">Jesus referred to this passage in Matthew 22:41-46; Mark 12:35-36; Luke 20:39-44.</p>
                    <p class="mb-2">Peter referred to it in his sermon at Pentecost, Acts 2:34. It is very important.</p>
                    <p class="mb-0">The prophecy was the Messiah-Savior would be the Divine Lord Himself.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">II The Virgin Birth would be God coming to Live with Men</h4>
                    <p class="mb-2">Isaiah 7:14 "... and shall call His Name Immanuel." This is the Savior that was coming.</p>
                    <p class="mb-0">Matthew 1:23 tells us that Immanuel means "God with us." This came true at Bethlehem.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">III The Messiah, the Lord Jesus Christ was Given Divine Names in Prophecy</h4>
                    <p class="mb-2">Isaiah 9:6,7 "For unto us a child is born, unto us a Son is given ... and His name shall be called Wonderful, Counselor, The Mighty God, The Everlasting Father, The Prince of Peace."</p>
                    <p class="mb-2">Only the Lord God Himself could be the fulfillment of these names.</p>
                    <p class="mb-2">Wonderful — Only God is truly wonderful. See Luke 18:19, none good save the Lord God.</p>
                    <p class="mb-2">Counselor — Only the omniscient God could be the perfect Counselor, Psalm 16:7.</p>
                    <p class="mb-2">The Mighty God — The One prophesied to be the Messiah was to be God Himself.</p>
                    <p class="mb-0">The Everlasting Father — John 10:30, Jesus said, "I and My Father are one." In this text Jesus as man was born but Jesus as God was "given." (God always existed.)</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">IV The Christ who was Prophesied to Come was to be from "Everlasting"</h4>
                    <p class="mb-2">Micah 5:2 "But thou Bethlehem ... out of thee shall He come forth unto Me that is to be ruler in Israel; whose goings forth have been from of old, from everlasting."</p>
                    <p class="mb-0">The eternal omnipresent God Himself was to be the Messiah-Savior.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">V God and Christ both Gave their Personal Names as "I AM"</h4>
                    <p class="mb-2">Exodus 3:14 "And God said unto Moses, I AM THAT I AM: and He said, Thus shalt thou say unto the children of Israel, I AM hath sent me unto you."</p>
                    <p class="mb-2">John 8:58 "Jesus said unto them, Verily, verily, I say unto you, Before Abraham was, I AM."</p>
                    <p class="mb-2">The Jews understood Jesus perfectly and thought that He had committed blasphemy for ascribing the Name of God to Himself.</p>
                    <p class="mb-0">They promptly attempted to stone Jesus. Death by stoning was the proper death penalty for this particular sin, Leviticus 24:12-16. But Jesus had not sinned for He was truly God. He was the great I AM in person.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">VI Jesus Christ Forgave Sins</h4>
                    <p class="mb-2">Mark 2:5 "Son, thy sins be forgiven thee." Only God can forgive sins.</p>
                    <p class="mb-2">Mark 2:7 "Why doth this man thus speak blasphemies? who can forgive sins but God only."</p>
                    <p class="mb-2">The Jews were correct for only God can forgive sin, for sin is transgression against the law and only the aggrieved person can forgive the guilty one.</p>
                    <p class="mb-0">In forgiving sin Jesus either blasphemed or was God. Jesus was God and could forgive.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">VII Jesus Claimed Equality with the Father and the Holy Spirit</h4>
                    <p class="mb-0">This is evident in the baptismal formula of Matthew 28:19, and in the benediction of 2 Corinthians 13:14 — the names of God the Father, Son and Holy Spirit are linked together.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">VIII Jesus Claimed Omnipresence</h4>
                    <p class="mb-2">To be omnipresent is a characteristic of God Almighty only, not true of angels or men.</p>
                    <p class="mb-0">Matthew 18:20 Jesus promises to be anywhere, everywhere at one and the same time.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">IX Jesus Claimed Omniscience</h4>
                    <p class="mb-2">Omniscience is to know all things and is a characteristic exclusive of God Himself.</p>
                    <p class="mb-0">This fact is evident in Mark 11:2-6, the description of the colt for the triumphant entry. Jesus exercised the gift of prophecy, too, in Matthew 12:40 and 24:3-31.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">X Jesus Claimed Omnipotence</h4>
                    <p class="mb-2">Omnipotence, all power, is another exclusive attribute of the Lord God Almighty.</p>
                    <p class="mb-0">Matthew 28:18 "All power is given unto Me in heaven and in earth." This claim supersedes the power of man, spirit, angels, demons, or supermen.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">XI Jesus Christ had Creative Power</h4>
                    <p class="mb-2">To bring something into being from nothing, creation, is possible only to God. Jesus Christ was active in creation and continues to maintain the world, John 1:1-3; Hebrews 1:3.</p>
                    <p class="mb-0">Jesus used creative power to perform some miracles — water to wine and food for the 5,000.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">XII Jesus Christ the true God had Power over the Elements</h4>
                    <p class="mb-2">Luke 8:24 Jesus rebuked the wind and it obeyed Him and there was a great calm at once.</p>
                    <p class="mb-2">Mark 4:39 Jesus calmed the waves of the raging sea — a power not of man but of God only.</p>
                    <p class="mb-0">Matthew 14:25, 26 Jesus walked on the water. This ability is denied to ordinary men.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">XIII Jesus Christ Received Worship due only to God</h4>
                    <p class="mb-2">John 9:38 the man who had been healed of blindness recognizing the Savior, said, "Lord, I believe. And he worshipped Him." Jesus as God rightfully accepted this worship.</p>
                    <p class="mb-2">Luke 4:8 Jesus said to Satan at the time of the temptation, "Get thee behind Me, Satan: for it is written, Thou shalt worship the Lord thy God and him only shalt thou serve."</p>
                    <p class="mb-0">To worship anyone but the Lord God is a terrible sin. When Jesus accepted worship He was saying to the world, "I am the Lord."</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">XIV Jesus Accepted the Testimony of Thomas</h4>
                    <p class="mb-2">John 20:28 "And Thomas answered and said unto Him, my Lord and my God."</p>
                    <p class="mb-0">If Thomas had made a mistake, misstatement or overstatement Jesus would have corrected it. Thomas had told the truth for Jesus was the Lord God, the eternal pre-existent One.</p>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">XV The Fact of Jesus' Resurrection</h4>
                    <p class="mb-2">This is the strongest and supreme argument that Jesus is God.</p>
                    <p class="mb-2">Romans 1:4 "And declared to be the Son of God with power, according to the spirit of holiness, by the resurrection from the dead."</p>
                    <p class="mb-2">This verse specifically states that the resurrection declares Jesus as the Son of God.</p>
                    <p class="mb-2">If Jesus were a fake, liar, impostor or a mere man or superman, God would have left Him in the grave until the day of the final judgment and condemned Him to hell.</p>
                    <p class="mb-2">According to Romans 6:4, it was the Father God, Himself, that raised Jesus from the dead.</p>
                    <p class="mb-0">The Father God thus proclaims conclusively to the world the deity of Jesus Christ.</p>
                  </div>
                </div>
              </div>

              <div id="conclusion" class="guide-section mb-5">
                <div class="section-header">
                  <i class="bi bi-bookmark-check section-icon"></i>
                  <h3 class="section-title">Conclusion</h3>
                </div>
                <div class="section-content">
                  <div class="content-card">
                    <p class="mb-2">We conclude that Jesus is God for He possesses the ten exclusive attributes of God.</p>
                    <ol class="mb-0 ms-3">
                      <li class="mb-2">Eternal — Micah 5:2 "... from everlasting." Also John 8:58; Colossians 1:17; Revelation 1:8.</li>
                      <li class="mb-2">Unchangeable — Hebrews 13:8 "Jesus Christ the same yesterday, and today, and for ever."</li>
                      <li class="mb-2">Omnipotent — Luke 8:24, Jesus stilled the tempest and the sea, also Matthew 28:18.</li>
                      <li class="mb-2">Omnipresent — Matthew 18:20; 28:20; John 1:48; 3:13.</li>
                      <li class="mb-2">Omniscient — Mark 11:2-6; John 2:24, 25; Luke 5:22; Matthew 24:3-31.</li>
                      <li class="mb-2">Holy — Mark 1:24 "Jesus ... the Holy One of God." Sinless 1 Peter 2:22; John 19:4.</li>
                      <li class="mb-2">Just — John 2:14-17, the cleansing of the Temple; Acts 17:31, a righteous Judge.</li>
                      <li class="mb-2">Loving — John 15:13 "Greater love hath no man than this." Also John 11:36.</li>
                      <li class="mb-2">Merciful — Titus 3:5 "According to His mercy He saved us." He died for us.</li>
                      <li class="mb-0">Faithful — 2 Timothy 2:13 "If we believe not, yet He abideth faithful."</li>
                    </ol>
                  </div>
                </div>
