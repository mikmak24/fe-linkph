                    <p class="mb-3"><strong>1. Shamrock</strong> — a three-leaved plant said to have been used by Saint Patrick to illustrate this doctrine. It is but one leaf but it has three distinct sections.</p>
                    <p class="mb-3"><strong>2. Water</strong> — revealed in three forms — liquid, ice and steam, yet all three have the same chemical formula, H₂O.</p>
                    <p class="mb-3"><strong>3. Light</strong> — red heat rays that are invisible picturing the Father, yellow light rays that are seen picturing the Son, blue chemical rays that are seen by their effects, picturing the Spirit.</p>
                    <p class="mb-3"><strong>4. Business firm</strong> (Smith & Co., three brothers, John, Henry and Peter) — It is one firm with one name but each brother is head of a department. The three work together without friction as a single unit as does the Lord.</p>
                    <p class="mb-3"><strong>5. Alban Douglas</strong> — I am only one person but I can be revealed as three persons: My mother sees me as a son. She sees me differently from anyone else and I respond to her in a way that is different from my response to others. My children see me as a father with all that involves. The class sees me as a teacher which is a different relationship. I remain one person but I am revealed as a son, father and teacher at the same time. God is one indivisible God revealed in three separate Persons.</p>
                    <p class="mb-0">It must be emphasized that the Trinity remains a mystery and that no single illustration can possibly explain everything. However, I believe that we can use these various illustrations to throw some light on this difficult and complex problem. Because God is a Spirit and we are physical it is hard for us to comprehend God. God is infinite while we are finite. To attempt a philosophical explanation of the tri-unity of God is an attempt to put the facts of the infinite into the finite.</p>
