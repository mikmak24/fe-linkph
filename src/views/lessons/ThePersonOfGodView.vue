                    <p class="mb-3"><strong>Psalm 139:7-9</strong> "Whither shall I go from Thy Spirit? or whither shall I flee from Thy presence? If I ascend up into heaven, Thou art there: If I make my bed in hell, behold Thou art there. If I take the wings of the morning, and dwell in the uttermost parts of the sea; even there shall Thy hand lead me."</p>
                    <p class="mb-3"><strong>Jeremiah 23:23</strong> "Am I a God at hand, saith the Lord, and not a God afar off?"</p>
