                    <p class="mb-3"><strong>Jeremiah 32:27</strong> "Behold I am the Lord, the God of all flesh: is there any thing too hard for Me?" Certainly not, for He can do anything or everything.</p>
                    <p class="mb-3"><strong>1 Kings 8:27</strong>.</p>

