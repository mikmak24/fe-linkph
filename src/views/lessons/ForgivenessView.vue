                      <p>This question was propounded in <strong>Romans 6:1</strong>, and answered by the Lord through Paul in <strong>Romans 6:2</strong>, God forbid, for such a thought is anathema to the Lord.</p>
                    </div>
                    <div class="frequency-section mb-3">
                      <h5 class="text-primary">How often should we forgive someone who sins against us?</h5>
