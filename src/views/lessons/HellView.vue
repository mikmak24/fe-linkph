                      <li>This doctrine is inconsistent with the love of God. The same Bible that speaks of the love of God also speaks of the justice and holiness of God. God is balanced, both loving and just.</li>
                      <li>Is there any need of eternal punishment? Won't all sin be paid for eventually? The punishment for each sin is death. <strong>Rom. 6:23</strong>, "For the wages of sin is death …"</li>
                      <li>Is it not unjust to sentence men to eternal punishment for one sin? Everlasting punishment does not mean equal punishment. The time may be the same, but the degrees of punishment will vary.</li>
                      <li>Won't the doomed repent and seek the Lord and be forgiven in hell? Salvation is limited to this life. All prayers of the doomed are denied. <strong>Luke 16:24-27</strong>. Prayers for deliverance and salvation will be denied forever.</li>
                      <li>Is it not cruel to think of the doomed being driven into perdition? The ungodly are relieved to get away from the holy presence of a righteous God.</li>
                      <li>The word "eternal" does not always mean forever and forever. It is the same word used in relation to God, <strong>Rom. 16:26</strong>, "The everlasting God," to life, <strong>John 3:16</strong>, "everlasting life," or rewards, <strong>Heb. 9:15</strong>, "eternal inheritance."</li>
                      <li>Surely you do not believe in a literal lake of fire? Whether it is literal or symbolical the point is that it is a place of punishment for the bodies and souls of men, angels and devils that reject Him.</li>
                      <li>Surely you don't teach that God tortures the lost! Conscience and memory will torture one in hell.</li>
                      <li>Don't you think hell is a state and not a place? Perhaps hell is both a state and a definite place.</li>
                      <li>Does "hell" not mean "grave" where both the good and the bad must go? In <strong>Luke 16</strong>, both men died and both men went to the grave, for both were buried, but the abodes of the souls of the two men were different. One was in a place where he was comforted, the other in a place of torment, and the two were separated by a great gulf to prevent travel back and forth.</li>
                      <li>Does "hell" or "hades" or "Gehenna" or "Sheol" mean annihilation only? The annihilationist says that in <strong>2 Thess. 1:9</strong>, "Who shall be punished with everlasting destruction from the presence of the Lord, and from the glory of his power." I believe that it is the punishment that is eternal and the subject must be conscious to be punished.</li>
                      <li>Will there not be a probationary period after death? Certainly not. Consider <strong>Heb. 9:27; Mark 9:42-50; Matt. 18:8, 9, 25-46</strong>.</li>
                    </ul>
                  </div>

