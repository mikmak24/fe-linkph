                      <li><strong>Spiritual drowsiness.</strong> In Pilgrim's Progress, Pilgrim fell asleep and lost his scroll. Often Christians fall asleep and lose both their joy and assurance of salvation. If this has happened you must confess your sins according to <strong>1 John 1:9</strong>, and start again.</li>
                      <li><strong>Worldliness.</strong> The love of the world chokes out desires for God, the Word and prayer.</li>
                      <li><strong>Lack of the fullness of the Holy Spirit, John 7:37-39.</strong> This is often caused by lack of surrender to the control of the Spirit in my life. Cure – Do not try to serve two masters. Yield your will to Him and He will guide.</li>
                      <li><strong>Over-emphasis on doctrines like predestination, election, or carnal or eternal security.</strong></li>
                      <li><strong>Sin and Backsliding:</strong> Cure – <strong>1 John 1:9</strong>.</li>
                    </ol>
