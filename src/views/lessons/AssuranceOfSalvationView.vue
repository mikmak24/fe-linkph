                        <li><strong>Acts 13:39</strong>, And by Him all that believe are justified from all things. (those who meet the condition of believing are justified)</li>
                        <li><strong>Luke 7:48</strong>, And He (Jesus) said unto her, Thy sins are forgiven. (When the sins are removed, the path to heaven is open and clear.)</li>
                        <li><strong>Roman 10:13</strong>, For whosoever shall call upon the name of the Lord shall be saved. (If I have called on the Lord Jesus, then I am saved.)</li>
