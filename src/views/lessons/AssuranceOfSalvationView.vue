                      <li>Do I truly believe on the Lord Jesus Christ? If I can answer a definite "yes" to both questions, then on the basis of the Word of God I am saved. To doubt the Word of God is a terrible sin casting shame on God's Holy Bible. It is not presumption to believe the Word of God. It is faith that honors and pleases God that rejoices His heart, <strong>Heb. 11:6</strong>.</li>
                    </ol>
                  </div>

