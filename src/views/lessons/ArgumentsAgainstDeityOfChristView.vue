                  <h3 class="section-title">The Arguments</h3>
                </div>
                <div class="section-content">
                  <div class="content-card mb-4">
                    <p class="mb-3">Christ is not God, because ...</p>

                    <p class="mb-2"><strong>I</strong> because He had flesh and bones, and God — being a spirit — has neither flesh nor bones — <strong>John 4:24; Luke 24:39</strong>.</p>
                    <p class="mb-2"><strong>Answer:</strong> Jesus Christ as man had both flesh and bones but as God He was spirit. This objection arises from the problem of the dual nature of the Savior.</p>
                    <p class="mb-2">In order for the invisible God to become visible He must become flesh and bones.</p>
                    <p class="mb-2"><strong>1 Timothy 2:5</strong> Jesus is a dual personality, "The man Christ Jesus," (man and God).</p>
                    <p class="mb-3"><strong>1 Timothy 3:16</strong> "God was manifest in the flesh," in the person of Jesus Christ.</p>

                    <p class="mb-2"><strong>II</strong> Because Christ had a beginning, and God had no beginning — <strong>John 8:42; Psalm 90:2</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> Jesus as a man had a beginning when He was conceived of the Holy Spirit but Jesus as God is without beginning and without end. <strong>John 8:56-59; Colossians 1:17; John 1:18; Isaiah 9:6</strong>.</p>

                    <p class="mb-2"><strong>III</strong> Because He has been created, and God has no beginning — <strong>Colossians 1:15; Revelation 3:14</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> A true translation of Revelation 3:14 is something like this, "He was the witness of the beginning of the creation of God;" not a creation but a witness of the creation. <strong>Proverbs 8:22, 23</strong> speaking of wisdom personified in the person of Christ, "Christ was from everlasting, from the beginning or ever the earth was ..."</p>

                    <p class="mb-2"><strong>IV</strong> Because Hosea 11:9 says that God is not a man, while Ezekiel 28:2 says that man is not God.</p>
                    <p class="mb-3"><strong>Answer:</strong> Hosea does not say that God could not assume human form of body and flesh. Nothing is too hard for God, <strong>Jeremiah 32:17,18</strong>, and He can be manifest in the flesh.</p>

                    <p class="mb-2"><strong>V</strong> Because He called God His Father — <strong>Matthew 27:46 and John 20:17</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> Jesus as the human Person called God His Father. God in <strong>Hebrews 1:8</strong> calls Jesus "God," but that does not lessen the Father's position of deity.</p>

                    <p class="mb-2"><strong>VI</strong> Because the Father sent Him to earth — <strong>John 8:42</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> The Father sending the Son to earth (Jesus volunteered to come) does not lessen His position as being the Almighty God. <strong>Luke 19:10; 1 Timothy 1:15; Galatians 4:4,5</strong>.</p>

                    <p class="mb-2"><strong>VII</strong> Because He appeared with flesh — <strong>1 John 4:2,3; 2 John 7</strong>. Since Jesus is flesh, bones and blood, He is merely a man and no more.</p>
                    <p class="mb-3"><strong>Answer:</strong> Jesus assumed flesh, bones and blood merely for the incarnation. As God He is eternal but to be our Redeemer it was necessary that He become partaker of humanity.</p>

                    <p class="mb-2"><strong>VIII</strong> Because God is His head — <strong>1 Corinthians 11:3</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> In a triumvirate it is necessary that one be the chairman but that does not mean that he is greater than the other two. The Father, Son and Holy Spirit are equal but for administrative purposes the Father acts as the executive administrator.</p>

                    <p class="mb-2"><strong>IX</strong> Because He is called man — <strong>John 8:40; 1 Timothy 2:5</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> Jesus was both true God and true man in one person without an intermingling of the two natures. Emmanuel means "God with us."</p>

                    <p class="mb-2"><strong>X</strong> Christ is not God, but He is a Son of God, just as we may become sons of God.</p>
                    <p class="mb-3"><strong>Answer:</strong> By conversion we become a "son of God" (<strong>John 1:12</strong>) but Jesus Christ is the "only begotten Son of God" (<strong>John 3:16</strong>), a unique and special position. God's Son is equal to the Father, <strong>Philippians 2:6</strong>, and the saint becomes an heir with Christ.</p>

                    <p class="mb-2"><strong>XI</strong> Because the Father gave Him power — <strong>Matthew 28:18</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> In <strong>Philippians 2:5-8</strong> the Son surrenders this power and God restores or returns it to Him after the resurrection. It was always His but He voluntarily yielded it.</p>

                    <p class="mb-2"><strong>XII</strong> Because He was made Lord by God — <strong>Acts 2:36</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> It is not made in the sense of a promotion for though He always was God, the Father takes this opportunity to proclaim this fact to all humanity.</p>

                    <p class="mb-2"><strong>XIII</strong> Because He is subject to God — <strong>1 Corinthians 15:28</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> This does not refer to Christ's nature but rather to His position. Christ voluntarily chose subjection. It was not imposed on Him against His will.</p>

                    <p class="mb-2"><strong>XIV</strong> Because Christ died and God is immortal and cannot die — <strong>1 Timothy 1:17; Luke 23:46 and Acts 2:32</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> Jesus as man died but Jesus as God could not and did not die, <strong>Hebrews 2:9, 14</strong>. It is the physical body that dies. The spirit does not die for it returns to God.</p>

                    <p class="mb-2"><strong>XV</strong> Because He was conceived by the Holy Spirit — <strong>Matthew 1:18-20</strong>. As a creature of the Holy Spirit He is therefore neither God nor Creator.</p>
                    <p class="mb-3"><strong>Answer:</strong> The humanity of Jesus Christ was conceived of the Holy Spirit. The deity of Christ has always existed, <strong>John 1:1,2</strong>. Jesus existed long before the Virgin Mary.</p>

                    <p class="mb-2"><strong>XVI</strong> Because He said "I ascend unto my Father and your Father: and to my God and your God" — therefore the two must be quite distinct — <strong>John 20:17</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> The Father and Son are two persons but one God, "I and My Father are one," <strong>John 10:30</strong>. See also <strong>John 17:11, 21, 22</strong>.</p>

                    <p class="mb-2"><strong>XVII</strong> Because He prayed to the Father and addressed Him as the only true God — <strong>John 17:3 and 1 Corinthians 8:6</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> This objection ignores the fact that the Son co-exists with the Father. They are one. They are one in nature (eternal Spirit), but distinct as to office and responsibilities. The Father and Son are indivisibly linked together, <strong>Matthew 11:27; Luke 10:22; John 12:45</strong>.</p>

                    <p class="mb-2"><strong>XVIII</strong> Because if He were God, then there would be more than one God.</p>
                    <p class="mb-3"><strong>Answer:</strong> This objection is dealt with in lesson 4, under the Trinity.</p>

                    <p class="mb-2"><strong>XIX</strong> Because He was only an "idea," "will" or "purpose" of God, having no material existence or material form in the beginning.</p>
                    <p class="mb-3"><strong>Answer:</strong> This is perhaps a play on the words in <strong>John 1:1,2</strong>, but verse 1 answers it definitely and authoritatively, "The Word (Jesus) was God."</p>

                    <p class="mb-2"><strong>XX</strong> Because He says, "My Father is greater than I" — <strong>John 14:28</strong>.</p>
                    <p class="mb-3"><strong>Answer:</strong> This refers to His position and certainly not to His being or nature. Positionally Jesus has chosen a position in subjection to His Father God.</p>

                    <p class="mb-2"><strong>XXI</strong> Christ is not God, because though He is One with God, that refers merely to Pastoring (shepherding), Teaching and Judgment, but not Godhead.</p>
                    <p class="mb-0"><strong>Answer:</strong> The Father and Son are one God (but two persons), co-existing as one Jehovah God. The Father was the Jehovah of the Old Testament, <strong>Psalm 23:1</strong>, Jesus in the New Testament, <strong>John 10:11; Hebrews 13:20; 1 Peter 2:25; Revelation 7:17</strong>.</p>
                  </div>
