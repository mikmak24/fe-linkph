                      <li class="mb-2">It is unchangeable truth. This doctrine stands with the Scriptures and does not change with the changing times, thoughts and theories of mankind, Hebrews 13:8.</li>
                      <li class="mb-2">It is urgent truth. This doctrine is essential to the plan of salvation. Without a virgin-born Savior, the salvation of one soul is utterly impossible.</li>
                      <li class="mb-2">It is undiscerable truth. It is a mystery hidden in God. There is a sense in which all life and birth is a mystery. How can a tree put life into its seed? 1 Corinthians 4:1, we Christians are to be good stewards of "the mysteries of God."</li>
                      <li class="mb-2">It is unconditional truth. I believe that this truth must be accepted as supreme. Romans 10:9, we are to confess Jesus as Lord and if He were not virgin-born, then He cannot be the Savior for He has broken the prophecies of Holy Scripture.</li>
                      <li class="mb-2">It is useful truth for the Almighty God has now entered the world as a man — but a sinless man, a man without a sin nature, a man who can save others. "Emmanuel" means "God with us." This is now true, for God has come to live on earth.</li>
                      <li class="mb-0">It is unavoidable truth for we face it yearly as we hear the Christmas story. Let us ceaselessly in our teaching present the virgin-born Savior to the world.</li>
                    </ol>
                  </div>

                  <div class="content-card mb-4">
                    <h4 class="text-primary mb-3">VI The Significance of the Virgin Birth</h4>
                    <p class="mb-2">Jesus Christ was not a phantom. He was a real human being, truly man, bone of our bone and flesh of our flesh, Philippians 2:7; He has become our "elder brother."</p>
                    <p class="mb-2">Jesus Christ was also God and remained God because of the Holy Spirit conception.</p>
                    <p class="mb-0">Jesus Christ was sinless because He was virgin-born without inheriting a fallen sinful, corrupt human nature. He was without original sin.</p>
                  </div>

