                      <li class="mb-2">Scientists do not accept it for it is not biologically true. Jesus did not come by natural conception but by a miracle conception.</li>
                      <li class="mb-2">Others reject it because it is miraculous. All life is a miracle, wonderful, mysterious.</li>
                      <li class="mb-0">Liberal theologians do not accept the doctrine. That does not alter the fact that it is true, for it is taught in the Holy Scriptures in prophecy and history.</li>
