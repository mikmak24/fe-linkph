                      <li class="mb-2">To bridge the chasm between God and man: 1 Timothy 2:5, "For there is one God, and one Mediator between God and men, the Man Christ Jesus."</li>
                      <li class="mb-2">To save men: Hebrews 2:14, 16, this was the basic purpose that brought Christ to earth.</li>
                      <li class="mb-0">To rescue the whole creation: Romans 8:19-22 "We know that the whole creation groaneth and travaileth in pain together until now ... waiting for ... redemption ..."</li>
                    </ol>
                  </div>

