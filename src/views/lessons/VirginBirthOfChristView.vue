                    <p class="mb-2">Paul, writing in Colossians 1:15-17, gives a picture of the greatness of the Lord Jesus Christ.</p>
                    <p class="mb-2">Matthew explicitly states that Jesus was conceived by the Holy Spirit and born of a virgin, Matthew 1:18, 20, 23.</p>
                    <p class="mb-2">Luke, a medical doctor, would be most interested in this medical phenomenon. Every child must have a father. Who was the Father and we are yet in our sins?</p>
                    <p class="mb-2">Luke carefully explains the unusual conception in Luke 1:27,31,34,35. Luke 1:34 Mary asks the sensible question of the angel, How can I have a child when I am not married and inferred that she was a pure virgin abhorring immorality.</p>
                    <p class="mb-0">Luke 1:35 the angel carefully explained that the Holy Spirit would be the Father.</p>
                  </div>

