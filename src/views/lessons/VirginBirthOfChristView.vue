                    <p class="mb-2">Mary was engaged to Joseph but they were not married yet. Christ was born of a virgin but a betrothed virgin so that she could be married to protect her good name.</p>
                    <p class="mb-2">Mary's conception was definitely from the Holy Spirit and not from her fiance Joseph or any other man whatsoever. God guarantees this to give us a perfect Savior.</p>
                    <p class="mb-2">The Lord encouraged Joseph to marry Mary to be a companion and guide to her in order to protect her from the extremity of the law, Leviticus 20:10.</p>
