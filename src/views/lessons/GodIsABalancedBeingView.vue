                    <p class="mb-3"><strong>Psalm 103:11, 17</strong> "For as the heaven is high above the earth, so great is His mercy toward them that fear Him. But the mercy of the Lord is from everlasting to everlasting upon them that fear Him."</p>
                    <p class="mb-3"><strong>Psalm 32:10</strong> "He that trusteth in the Lord, mercy shall compass him about."</p>
                    <p class="mb-3"><strong>Psalm 51:1</strong> "Have mercy upon me, O God, according to Thy loving kindness."</p>
